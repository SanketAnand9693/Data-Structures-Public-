/*
To print below pattern
 *  *  *  *
 *  *  *  *
 *  *  *  *
 *  *  *  *
 */

public class _3PatternRectangle {
    public static void main(String [] args)
    {
        for (int i=0;i<4;i++)
        {
            for(int j=0;j<4;j++)
            {
            System.out.print(" * ");
            }
            System.out.println();
        }
    }
    
}
